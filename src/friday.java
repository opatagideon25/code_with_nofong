public class friday {
    public static void main(String[] args) {
        maths(40, 60);
    }

    private static void maths(int x, int y) {
        System.out.println(x +"+"+ y +"="+(x + y));
        System.out.println(x +"-"+ y +"="+(x - y));
        System.out.println(x +"*"+ y +"="+(x * y));
        System.out.println(x +"%"+ y +"="+(x % y));
        System.out.println(x +"/"+ y +"="+(x / y));
    }
}
